
public class A2WQ1 {
  public static void main(String[] args) {
 
    char ch = ' ';
    printTypeOfChar(ch);
  }
    public static void printTypeOfChar(char ch){
    if(ch >= 'A' && ch <= 'Z') {
      System.out.println("UPPERCASE");
    }
    else if(ch >= 'a' && ch <= 'z') {
      System.out.println("lowercase");
  }
    else {
      System.out.println("Symbol");
    }
    
  }
}   
  
  
  
     
       

        
  