
public class HelloWorld {                    // declare class 

  public static void main (String[] args){   // args is an array created for command line instructions. For e.g, if you wrote "Java MyProgram one two" on command line
                                             // then args will contain [one, two]
                                             // Doesn't have to be named "args".
                                             // It's a form for standardization. Oracle does not know how many arguments a programmer will need and what types of them. 
                                             // For this reason, with the 'args[]' of type String you can pass N arguments to you program, and convert as you need          ,
                                             // because a String can become whatever primitive type in Java.
   
    System.out.println("Hello World");      // print Hello World
  }
  
}

  